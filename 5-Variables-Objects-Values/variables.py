#!/usr/bin/python


def main():
    num = 42
    print(type(num), num)

    num2 = float(42)
    print(type(num2), num2)

    num3 = 42 // 9
    print(type(num3), num3)

    num4 = 42 / 9
    print(type(num4), num4)

    num5 = round(42 / 9, 2)
    print(type(num5), num5)

    num6 = 42 % 9
    print(type(num6), num6)

    num7 = int(42.9)
    print(type(num7), num7)





if __name__ == "__main__":main()